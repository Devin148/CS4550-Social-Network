        	<h1>About</h1>
        	<p>"Northeastern Social" is a social networking site created by Devin Hurd and Alexander Seeto as a final project for Northeastern University course "CS4550: Web Development" taught by Professor Harriet Fell.</p>
            <p>"Northeastern Social" allows the Northeastern community to connect online more exclusively.  The site enables users to share their co-op experiences on a more personal level and makes this information viewable for all users in an organized manner.  Users provide the steps they took in setting up their co-op interview, specific questions they were asked during their interview, and overall assessments of past co-ops.</p>
            <p>The site doubles as a platform where users can update statuses about their daily lives.  It enables users to meet colleagues at Northeastern University
            as well as connect with friends.</p>
            <p>Our goal in creating this project is to aid Northeastern students in finding quality co-op positions that will help build their  careers as well as to expand their professional networks.</p>
